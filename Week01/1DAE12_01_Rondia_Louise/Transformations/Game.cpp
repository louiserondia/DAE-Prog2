void Game::Draw( ) const
{
	ClearBackground( );

	glPushMatrix();
	{ // braces to express begin and end of that part, not mandatory
		
		glTranslatef(100.f, 100.f, 0.f);
		glRotatef(m_CurrentAngle, 0.f, 0.f, 1.f);
		glScalef(1.5, 1.f, 1.f);

		glTranslatef(m_pTexture->GetWidth() / -2.f, m_pTexture->GetHeight() / -2.f, 0.f);
		m_pTexture->Draw();
	}
	glPopMatrix();
}

